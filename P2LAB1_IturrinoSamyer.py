# import math library
import math


 

# Example usage
radius = float(input("What is the radius of the circle \n"))

# Calculate

diameter = 2 * radius
area = math.pi * radius ** 2
circumference = 2 * math.pi * radius


print(f"The diameter of the circle is {diameter}\n")
print(f"The circumference of the circle is {circumference:.2f}\n")
print(f"The area of the circle is {area:.2f}\n")
